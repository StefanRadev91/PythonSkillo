# Create a program that asks the user to enter two numbers and 
# then prints out the sum, difference, product, and quotient of those numbers.

number_one = int(input("Please enter the first number: "))
number_two = int(input("Please enter the second number: "))

print(number_one + number_two)
print(number_one - number_two)
print(number_one * number_two)
print(number_one / number_two)
